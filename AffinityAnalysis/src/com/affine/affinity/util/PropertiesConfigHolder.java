package com.affine.affinity.util;

public class PropertiesConfigHolder {
   boolean flag;
   String configPropLoc;
   String url;
   String user;
public boolean isFlag() {
	return flag;
}
public void setFlag(boolean flag) {
	this.flag = flag;
}
public String getConfigPropLoc() {
	return configPropLoc;
}
public void setConfigPropLoc(String configPropLoc) {
	this.configPropLoc = configPropLoc;
}
public String getUrl() {
	return url;
}
public void setUrl(String url) {
	this.url = url;
}
public String getUser() {
	return user;
}
public void setUser(String user) {
	this.user = user;
}
   
	
}
